<!-- WHY YOUR BATH IS PEELING -->
<section class="py-16 sm:py-20 bg-white">
 <div class="max-w-7xl mx-auto px-6 sm:px-8">
 <h2 class="text-3xl sm:text-4xl font-extrabold text-primary tracking-tighter mb-4">Why Your Bath Is Peeling</h2>
 <p class="text-secondary max-w-3xl mb-10">Bath coatings don't just peel for no reason. Understanding the cause helps you see why the old coating must come off completely before a new one can go on.</p>

 <div class="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-6">
 <div class="bg-surface-container-low rounded-xl p-6 reveal">
 <span class="material-symbols-outlined text-3xl text-error mb-3 block" aria-hidden="true">format_paint</span>
 <h3 class="font-bold text-primary mb-2">Previous DIY Paint Job</h3>
 <p class="text-sm text-secondary leading-relaxed">Hardware store bath paint is a single-part product designed for quick cosmetic touch-ups. It sits on the surface rather than bonding to it, and typically starts peeling within 6 to 12 months. It was never designed to be a permanent solution.</p>
 </div>
 <div class="bg-surface-container-low rounded-xl p-6 reveal">
 <span class="material-symbols-outlined text-3xl text-error mb-3 block" aria-hidden="true">layers_clear</span>
 <h3 class="font-bold text-primary mb-2">Poor Surface Prep</h3>
 <p class="text-sm text-secondary leading-relaxed">Even a decent coating will fail if the surface wasn't properly prepared. If the old coating was applied over a dirty, glossy, or unetched surface without proper priming, it never had a chance of lasting. Adhesion starts with preparation.</p>
 </div>
 <div class="bg-surface-container-low rounded-xl p-6 reveal">
 <span class="material-symbols-outlined text-3xl text-error mb-3 block" aria-hidden="true">schedule</span>
 <h3 class="font-bold text-primary mb-2">Age &amp; Wear</h3>
 <p class="text-sm text-secondary leading-relaxed">Even professional coatings have a lifespan. After 10 to 15 years of daily use, hot water, soap, and cleaning products, a coating will eventually start to break down. If your bath was resurfaced years ago, it may simply be due for a redo.</p>
 </div>
 <div class="bg-surface-container-low rounded-xl p-6 reveal">
 <span class="material-symbols-outlined text-3xl text-error mb-3 block" aria-hidden="true">science</span>
 <h3 class="font-bold text-primary mb-2">Chemical Damage</h3>
 <p class="text-sm text-secondary leading-relaxed">Abrasive cleaners, bleach-based products, and harsh chemicals strip the coating over time. They attack the surface layer, cause it to become brittle, and eventually it lifts and peels away from the substrate underneath.</p>
 </div>
 </div>
 </div>
</section>

<!-- THE PROBLEM WITH PAINTING OVER PEELING -->
<section class="py-16 sm:py-20 bg-surface-container-low">
 <div class="max-w-7xl mx-auto px-6 sm:px-8">
 <h2 class="text-3xl sm:text-4xl font-extrabold text-primary tracking-tighter mb-4">The Problem With Painting Over Peeling</h2>
 <p class="text-secondary max-w-3xl mb-10">It's tempting to just buy another tin of bath paint and go over the top. But here's why that approach fails every time.</p>

 <div class="grid grid-cols-1 md:grid-cols-2 gap-8 max-w-4xl">
 <div class="bg-white rounded-xl p-8 border-l-4 border-error reveal">
 <span class="material-symbols-outlined text-2xl text-error mb-3 block" aria-hidden="true">warning</span>
 <h3 class="font-bold text-primary mb-3">Why It Doesn't Work</h3>
 <p class="text-sm text-secondary leading-relaxed mb-4">When you paint over a peeling surface, the new coating bonds to the old flaking paint. Not to the bath itself. As the old coating continues to lift, it takes the new layer with it. You end up with the same problem again in weeks or months, plus you've now got two layers of failed coating to deal with.</p>
 <p class="text-sm text-secondary leading-relaxed">Each additional layer makes the next repair harder and more expensive. The old coating traps moisture between layers, accelerating the failure.</p>
 </div>
 <div class="bg-white rounded-xl p-8 border-l-4 border-primary reveal">
 <span class="material-symbols-outlined text-2xl text-primary mb-3 block" aria-hidden="true">check_circle</span>
 <h3 class="font-bold text-primary mb-3">What Needs to Happen</h3>
 <p class="text-sm text-secondary leading-relaxed mb-4"><strong>All old coating must be completely stripped back to the bare substrate.</strong> Every trace of the failed paint or coating needs to come off. The underlying surface then needs to be sanded, etched, and primed so the new coating bonds directly to clean material.</p>
 <p class="text-sm text-secondary leading-relaxed">It's about adhesion. A professional two-part acrylic coating applied to a properly prepared surface bonds at a molecular level. Not just sitting on top. That's the difference between a coating that lasts years and one that peels in months.</p>
 </div>
 </div>
 </div>
</section>

